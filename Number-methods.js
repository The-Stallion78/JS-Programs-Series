/*
toString():	Returns a number as a string

toExponential() :	Returns a number written in exponential notation

toFixed(): Returns a number written with a number of decimals

toPrecision():	Returns a number written with a specified length

valueOf():	Returns a number as a number
*/

let num = 5.51453;
console.log("\n----------------------------")
console.log("Original value: ", num)

// toString()
console.log("Converting toString value: ", num.toString())

// toExponential()
console.log("Converting value to Exponential: ", num.toExponential())

// toPrecision()
console.log("Converting value to Precision: ", num.toPrecision(2))

// valueOf()
console.log("Converting value to number: ", num.valueOf())
console.log("----------------------------\n")






















